package com.example.drawer;

public class SavedInformation {

        private String mLine1;
        private String mLine2;

        public SavedInformation(String line1, String line2) {
            mLine1 = line1;
            mLine2 = line2;
        }

        public String getLine1() {
            return mLine1;
        }

        public String getLine2() {
            return mLine2;
        }
}
